# Python提供了两种基本的循环结构语句——while和for。
# while循环一般用于循环次数难以提前确定的情况，也可以用于循环次数确定的情况。
# for循环一般用于循环次数可以提前确定的情况，尤其是用于枚举序列或迭代对象中的元素。
# 一般优先考虑使用for循环。
# 相同或不同的循环结构之间都可以互相嵌套，实现更为复杂的逻辑。
# for循环和while循环都可以带else。
# while 条件表达式:
# 	循环体
# [else:                     # 如果循环是因为break结束的，就不执行else中的代码
# 	else子句代码块]


# for 取值 in 序列或迭代对象:
# 	循环体
# [else:
#     else子句代码块]



